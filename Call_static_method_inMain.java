//Call static method inside main method

package Assignments;

public class Call_static_method_inMain 
{

	public static void quality ()
	{
		System.out.println("i am honest");
	}
	
	static void gender()
	{
		System.out.println("i am male");
	}
	
	static void city()
	{
		System.out.println("I live in Mumbai");
	}
	public static void main(String[] args) 
	
	{
		quality();
		gender();
		city();
	}	
	
}
